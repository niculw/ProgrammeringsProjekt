void initLevel( char block[25][20] , short level ){
	int i,j;
	switch ( level ) {
		case 1 :
			for (i = 0; i < 25; i++){
				for (j = 0; j < 20; j++){
					block[i][j] = LEVEL_1[i][j];
				}
			}
			break;
		case 2 :
			for (i = 0; i < 25; i++){
				for (j = 0; j < 20; j++){
					block[i][j] = LEVEL_2[i][j];
				}
			}
			break;
		case 3 :
				
			break;
	}
}

void printBlocks( char block[25][20] ) {
	int i,j,k;
	reverse('o');
	gotoxy( 2 , 2 );
	for ( i = 0 ; i < 25 ; i++ ){
		for ( j = 0 ; j < 20 ; j++ ){
			if ( block[i][j] && 0x01 == 0x01 ){		// tjek om blokken er aktiv 
				color( block[i][j] >> 4, 0);		// sætter farven på blokken
				gotoxy( 2 + ( j << 3 ) , 2 + (i << 1)  );
				for (k = 0; k < 8; k++){
					printf(" ");
				}
				gotoxy( 2 + ( j << 3 ) , 2 + (i << 1) + 1 );
				for (k = 0; k < 8; k++){
					printf(" ");
				}
			}
		}
	}
	reverse('p');
}


				gotoxy( 2 + ( j << 3 ) , 2 + (i << 1)  );	// går til blok position
				for (k = 0; k < 8; k++){					
					printf("%c", health);							// farver den øverste del af blokken
				}
				gotoxy( 2 + ( j << 3 ) , 2 + (i << 1) + 1 );
				for (k = 0; k < 8; k++){
					printf("%c", health);							//// farver den nederste del af blokken
				}
